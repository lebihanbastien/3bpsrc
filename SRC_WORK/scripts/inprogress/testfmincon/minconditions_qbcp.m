function [f] = minconditions_qbcp(x0, yeml2, yseml2, N)

%--------------------------------------------------------------------------
% Maneuver at t1
%--------------------------------------------------------------------------
% State at t1
y1 = x0(1:6);
% Maneuver at t1
f = norm(y1(4:6) - yeml2(4:6));

%--------------------------------------------------------------------------
% Maneuver at tN
%--------------------------------------------------------------------------
% State at tN
yN = x0((N-1)*6+1:N*6);
% Maneuver at tN
f = f + norm(yN(4:6) - yseml2(4:6));

end

